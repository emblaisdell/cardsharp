"""Train DMC + PPO self-play on each game and benchmark them vs random, vs the
TS linear model, and head-to-head. Writes docs/ml-pytorch-results.md.

    python -m ml.run_experiments [--seconds 60] [--games 30]

Budgets are deliberately modest (single CPU, minutes/game) — this demonstrates
the methods and the pipeline, not SOTA agents. Compute used is logged per game.
"""

import argparse
import os
import time

import torch

from cardsharp.parser import parse
from .net import CardNet
from .dmc import train as dmc_train, DMCAgent
from .ppo import train as ppo_train, PPOAgent
from .evaluate import eval_vs_random, match, RandomAgent
from .linear import LinearPolicy

ROOT = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", "..", ".."))

# (file, players, train-seconds-scale) — long games get a bit more wall-clock
GAMES = [
    ("blackjack", 2, 1.0),
    ("gofish", 2, 1.3),
    ("oldmaid", 3, 1.0),
    ("thirtyone", 2, 1.3),
    ("tableless", 2, 1.0),
    ("thewall", 2, 1.0),
    ("crazybridge", 3, 1.3),
    ("moneymoneymoney", 3, 1.3),
]


def load_net(path):
    ck = torch.load(path)
    net = CardNet(ck["hidden"])
    net.load_state_dict(ck["state_dict"])
    net.eval()
    return net, ck["info"]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--seconds", type=float, default=60.0)
    ap.add_argument("--games", type=int, default=30)
    ap.add_argument("--out", default=os.path.join(ROOT, "docs", "ml-pytorch-results.md"))
    ap.add_argument("--models", default=os.path.join(ROOT, "models", "py"))
    args = ap.parse_args()
    os.makedirs(args.models, exist_ok=True)

    rows = []
    for name, np_, scale in GAMES:
        secs = args.seconds * scale
        path = os.path.join(ROOT, "games", f"{name}.card")
        program = parse(open(path).read())
        print(f"\n=== {program.name} ({np_}p) — train {secs:.0f}s/method ===", flush=True)

        t = time.time()
        dmc_net, dmc_info = dmc_train(program, np_, seconds=secs)
        torch.save({"state_dict": dmc_net.state_dict(), "hidden": 128, "info": dmc_info},
                   os.path.join(args.models, f"{name}_dmc.pt"))
        ppo_net, ppo_info = ppo_train(program, np_, seconds=secs)
        torch.save({"state_dict": ppo_net.state_dict(), "hidden": 128, "info": ppo_info},
                   os.path.join(args.models, f"{name}_ppo.pt"))
        dmc_net.eval()
        ppo_net.eval()

        dmc = DMCAgent(dmc_net, epsilon=0.0)
        ppo = PPOAgent(ppo_net, greedy=True)
        lin = None
        lin_path = os.path.join(ROOT, "models", f"{name}.json")
        if os.path.exists(lin_path):
            lin = LinearPolicy.load(lin_path)

        g = args.games
        r = {"game": program.name, "np": np_}
        r["dmc_vs_rand"] = eval_vs_random(program, np_, dmc, games=g)
        r["ppo_vs_rand"] = eval_vs_random(program, np_, ppo, games=g)
        r["rand_vs_rand"] = eval_vs_random(program, np_, RandomAgent(1), games=g)
        if lin is not None:
            r["lin_vs_rand"] = eval_vs_random(program, np_, lin, games=g)
            a, b = match(program, np_, dmc, lin, games=g)
            r["dmc_vs_lin"] = (a, b)
            a, b = match(program, np_, ppo, lin, games=g)
            r["ppo_vs_lin"] = (a, b)
        a, b = match(program, np_, dmc, ppo, games=g)
        r["dmc_vs_ppo"] = (a, b)
        r["dmc_info"] = dmc_info
        r["ppo_info"] = ppo_info
        r["wall"] = time.time() - t
        rows.append(r)
        print(f"  {program.name}: DMC vs rand {r['dmc_vs_rand']:.2f}, "
              f"PPO vs rand {r['ppo_vs_rand']:.2f}, rand {r['rand_vs_rand']:.2f}", flush=True)
        write_report(rows, args)  # incremental

    write_report(rows, args)
    print("\nwrote", args.out)


def pct(x):
    return f"{100*x:.0f}%"


def write_report(rows, args):
    lines = []
    lines.append("# PyTorch self-play results (DMC & PPO)\n")
    lines.append("Auto-generated by `python -m ml.run_experiments` "
                 f"(--seconds {args.seconds:.0f} --games {args.games}). Single CPU.\n")
    lines.append("All numbers are **win rate** (shared wins count as a win), averaged "
                 "over every seat orientation to cancel turn-order bias. `vs random` "
                 "= one trained seat against random opponents. Head-to-head = one seat "
                 "of A vs the rest filled by B.\n")
    lines.append("## vs random opponents\n")
    lines.append("| Game | seats | DMC | PPO | linear | random |")
    lines.append("|---|--:|--:|--:|--:|--:|")
    for r in rows:
        lin = pct(r["lin_vs_rand"]) if "lin_vs_rand" in r else "—"
        lines.append(f"| {r['game']} | {r['np']} | {pct(r['dmc_vs_rand'])} | "
                     f"{pct(r['ppo_vs_rand'])} | {lin} | {pct(r['rand_vs_rand'])} |")
    lines.append("\n## head-to-head\n")
    lines.append("| Game | DMC vs linear | PPO vs linear | DMC vs PPO |")
    lines.append("|---|---|---|---|")
    for r in rows:
        dl = f"{pct(r['dmc_vs_lin'][0])} / {pct(r['dmc_vs_lin'][1])}" if "dmc_vs_lin" in r else "—"
        pl = f"{pct(r['ppo_vs_lin'][0])} / {pct(r['ppo_vs_lin'][1])}" if "ppo_vs_lin" in r else "—"
        dp = f"{pct(r['dmc_vs_ppo'][0])} / {pct(r['dmc_vs_ppo'][1])}"
        lines.append(f"| {r['game']} | {dl} | {pl} | {dp} |")
    lines.append("\n## compute used (per method, per game)\n")
    lines.append("| Game | DMC episodes / decisions / s | PPO episodes / decisions / s |")
    lines.append("|---|---|---|")
    for r in rows:
        d = r["dmc_info"]
        p = r["ppo_info"]
        lines.append(f"| {r['game']} | {d['episodes']} / {d['decisions']} / {d['seconds']:.0f}s "
                     f"| {p['episodes']} / {p['decisions']} / {p['seconds']:.0f}s |")
    lines.append("\n*DMC = Deep Monte-Carlo (DouZero-style); PPO = clipped policy "
                 "gradient with entropy bonus. Both share the same card-matrix + "
                 "per-option action-scoring network (`ml/net.py`). See "
                 "[ml-research.md](ml-research.md) for the method rationale.*\n")
    with open(args.out, "w") as f:
        f.write("\n".join(lines))


if __name__ == "__main__":
    main()
